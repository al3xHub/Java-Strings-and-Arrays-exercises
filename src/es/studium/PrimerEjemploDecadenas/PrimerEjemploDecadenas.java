package es.studium.PrimerEjemploDecadenas;

public class PrimerEjemploDecadenas
{

	public static void main(String[] args)
	{
		String cadena;
		cadena = "Hola mundo!";
		
		System.out.println(cadena);
		System.out.println(cadena.length()); //nos muestra en número entero cuantos caracteres tiene el objeto cadena.
		System.out.println(cadena.toUpperCase()); //nos muestra la cadena en mayúsculas
		System.out.println(cadena.toLowerCase()); //nos muestra la cadena en minúsculas
	}

}
